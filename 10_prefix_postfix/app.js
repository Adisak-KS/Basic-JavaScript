let a = 5, b = 10;

//การเพิ่มค่า
//Prefix
console.log("ค่าเริ่มต้น = ", a);
console.log("Prefix = ", ++a);
console.log("ค่าปัจจุบัน = ", a);

// Postfix
a = 5;
console.log("ค่าเริ่มต้น = ", a);
console.log("Postfix = ", a++);
console.log("ค่าปัจจุบัน = ", a);

// การลดค่า 
// Prefix
console.log("ค่าเริ่มต้น = ", b);
console.log("Prefix = ", --b);
console.log("ค่าปัจจุบัน = ", b);

//Postfix
console.log("ค่าเริ่มต้น = ", b);
console.log("Postfix = ", b--);
console.log("ค่าปัจจุบัน = ", b);